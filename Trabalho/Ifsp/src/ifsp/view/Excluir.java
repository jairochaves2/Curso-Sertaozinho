    /**
     *Busca as disciplinas no banco e insere no comboBox
     */
    private void setaDisciplinas() {

        listDisc = DisciplinaDAO.selectDisc();
        for (Disciplina disciplina : listDisc) {
            cbDisciplina.addItem(disciplina.getNome());
        }
    }
