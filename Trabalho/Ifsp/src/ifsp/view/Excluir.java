    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente EXCLUIR essa disciplina?");
        //verifica se o usuário escolheu seguir com a operação
        if (i == 0) {
            int itemSelecionado = cbDisciplina.getSelectedIndex();
            //se o campo não for o 'selecione', remove o item do banco, todos do ComboBox e readiciona
            
            if (!(itemSelecionado == 0)) {
                DisciplinaDAO.deletaDisc(listDisc.get(itemSelecionado - 1).getId());
                cbDisciplina.removeAllItems();
                cbDisciplina.addItem("Selecione...");
                setaDisciplinas();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

